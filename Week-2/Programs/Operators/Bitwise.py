a = 3
b = 2

op1 = a & b  # Bitwise AND
op2 = a | b  # Bitwise OR
op3 = a ^ b  # Bitwise XOR
op4 = ~a     # Bitwise NOT
op5 = a << 1  # Bitwise left shift
op6 = a >> 1  # Bitwise right shift

print("a & b (Bitwise AND):", op1)
print("a | b (Bitwise OR):", op2)
print("a ^ b (Bitwise XOR):", op3)
print("~a (Bitwise NOT):", op4)
print("a << 1 (Bitwise left shift):", op5)
print("a >> 1 (Bitwise right shift):", op6)