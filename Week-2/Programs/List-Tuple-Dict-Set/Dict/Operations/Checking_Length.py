# ------------------------------
# ✅ Checking Key Existence in Dictionary
# ------------------------------

# Creating a sample dictionary with 3 key-value pairs
dict1 = {'a': 1, 'b': 2, 'c': 3}

# Using 'in' operator to check if a key exists in the dictionary
# Returns True if key exists, False if it doesn't
print("Is 'a' in dictionary?", 'a' in dict1)  # True - key 'a' exists
print("Is 'd' in dictionary?", 'd' in dict1)  # False - key 'd' doesn't exist

# Output:
# Is 'a' in dictionary? True
# Is 'd' in dictionary? False


# ---------------------------
# ✅ Finding Length of Dictionary
# ---------------------------

# Using len() function to get the number of key-value pairs
# len() returns the total count of items in the dictionary
print("Length of dictionary:", len(dict1))

# Output:
# Length of dictionary: 3
