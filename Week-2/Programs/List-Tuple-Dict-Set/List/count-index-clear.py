# Count : This function is used to count the number of occurrences of a specific element in the list.

list1 = [10, 20, 30, 20, 50, 20]
count_of_20 = list1.count(20)  # Counts how many times 20 appears in the list
print("Count of 20:", count_of_20)

# Index : This function returns the index of the first occurrence of a specific element in the list.

list2 = [10, 20, 30, 20, 50]
index_of_20 = list2.index(20)
print("Index of 20:", index_of_20)  

# Clear : This function removes all elements from the list, making it empty.

list3 = [10, 20, 30, 40, 50]
list3.clear()  # Clears the list
print("List after clear:", list3)  