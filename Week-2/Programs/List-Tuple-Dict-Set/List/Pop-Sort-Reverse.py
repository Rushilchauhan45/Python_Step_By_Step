#Pop : It removes the last element from the list and returns it.

list1 = [10, 20, 30, 40, 50]
popped_element = list1.pop()  # LIFO - Last In First Out
print("Popped Element:", popped_element)
print("List after pop:", list1)


# Sort : It sorts the elements of the list in ascending order by default.

list2 = [5, 2, 9, 1, 5, 6]
list2.sort()  # Sorts the list in ascending order
print("Sorted List:", list2)

# Reverse : It reverses the order of elements in the list.

list3 = [1, 2, 3, 4, 5]
list3.reverse()  # Reverses the list
print("Reversed List:", list3)