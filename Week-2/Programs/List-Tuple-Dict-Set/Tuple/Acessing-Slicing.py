#Acess Any Indexof tuple
#You can Add Multiple Data Types in Tuple

tuple1 = (1, 2, 3, 4, 5)
print(tuple1[0])

#Slicing In Tuple

tuple2 = (10, "Python", 30.45, True, "JAVA", 60)

print(tuple2[1:6])
print(tuple2[:4])
print(tuple2[2:])  

#Negative Slicing in Tuple

print(tuple2[-3:])  
print(tuple2[-5:-1])  

