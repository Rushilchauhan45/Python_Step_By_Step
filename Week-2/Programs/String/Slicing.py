#Slicing 
#Slicing Must be Order in Left to Right 

str = "Python is a High-level programming language"

print(str[0:6])  # Output: Python
print(str[:10])  #Default start index is 0  
print(str[12:])  #Default end index is last index of string

#Negative Slicing

str1 = "Hellow , Python World!"

print(str1[-6:]) # Output: World!
print(str1[-13:-7]) # Output: Python






