# To Find Position of Any Character in String

str = "Hello Python World!"

print(str.find("World!"))

# To Count Occurrences of a Substring in a String

str = "Hello Python World!"

print(str.count("l"))

# To Find Strats with a Substring

str = "Hello Python World!"

print(str.startswith("Hello"))

# To Find Ends with a Substring

print(str.endswith("World!"))
