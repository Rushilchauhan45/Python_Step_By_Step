
# Basic Data Types in Python
var1 = 10 #integer
var2 = 20.5 #float
var3 = "Hello, World!" #string
var4 = True #boolean--> 1 or 0 
var5 = 4.3 + 5j #complex number

print(type(var1),var1)
print(type(var2),var2)
print(type(var3),var3)
print(type(var4),var4)
print(type(var5),var5)

# type() function returns the type of the variable
